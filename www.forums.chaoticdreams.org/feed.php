We really hope it to be sooner rather than later. But it's not all smooth sailing. The last build of the game from Epic has issues with folks trying to compile custom content, and we are currently unable to update. Hopefully the next update comes soon, And fixes the issues with custom content. <br /><br />Keep in mind of course, that any custom content created and released for UT4 these days, only works on the version it was created on. So, if we were to put out a version now, you wouldn't be able to update your game and still play the mod. You'd have to wait until the mod was updated also. <br /><br />Well, there is a way, but it involves renaming your current directory, so it doesn't get updated, and then re-downloading the upgrade. Which means you have two copies of the game installed instead of one. An old one for the mod, and the newest. That's what we have to do now for testing.<br /><br />Anyway. Point is we're working on it, but it's still a game being called 'pre-alpha', and putting out a mod right now is a complicated issue, that wouldn't be as easy as you remember from previous version of Chaos. <br /><br />Having said that, we are grateful you are watching and waiting!<p>Statistics: Posted by <a href="http://www.forums.chaoticdreams.org/memberlist.php?mode=viewprofile&amp;u=21">R.Flagg</a> — Thu Sep 29, 2016 6:43 pm</p><hr />
